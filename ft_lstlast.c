#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int	i;
	int	j;

	j = ft_lstsize(lst);
	i = 0;
	while (i < j - 1)
	{
		lst = lst->next;
		i++;
	}
	return (lst);
}
